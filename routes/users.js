const express = require('express');
const router = express.Router();
const passport = require('passport');
const catchAsync = require('../utils/catchAsync');
const User = require('../models/user');
const users = require('../controllers/users');

router.route('/register')
    .get(users.renderRegister)
    .post(catchAsync(users.register));

router.route('/login')
    .get(users.renderLogin)
    .post(passport.authenticate('local', { failureFlash: true, failureRedirect: '/login' }), users.login)

router.get('/logout', users.logout)

module.exports = router;
// This middleware function is provided by Passport.js to handle authentication.
// 'local' refers to the local strategy, which is commonly used for username and password authentication. Passport supports various authentication strategies (e.g., local, OAuth, OpenID, etc.).